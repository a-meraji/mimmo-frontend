/**
 * Get country by ISO code
 * @param {string} iso - ISO country code (e.g., 'IR', 'US', 'CA')
 * @returns {object|undefined} Country object or undefined if not found
 */
export const getCountryByISO = (iso) => {
  return COUNTRIES.find((country) => country.iso === iso);
};

/**
 * Get phone code by ISO code
 * @param {string} iso - ISO country code (e.g., 'IR', 'US', 'CA')
 * @returns {string|undefined} Phone code (e.g., '+98') or undefined if not found
 */
export const getPhoneCodeByISO = (iso) => {
  const country = getCountryByISO(iso);
  return country?.code;
};
